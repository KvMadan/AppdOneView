package com.km.appd;

/**
 * @author Madan Kavarthapu
 *
 */
public class MetricValues {
	
	private String occurrences;
	private String current;
	private String min;
	private String max;
	private String startTimeInMillis;
	private String useRange;
	private String count;
	private String sum;
	private String value;
	private String standardDeviation;
	/**
	 * @return the occurrences
	 */
	public String getOccurrences() {
		return occurrences;
	}
	/**
	 * @param occurrences the occurrences to set
	 */
	public void setOccurrences(String occurrences) {
		this.occurrences = occurrences;
	}
	/**
	 * @return the current
	 */
	public String getCurrent() {
		return current;
	}
	/**
	 * @param current the current to set
	 */
	public void setCurrent(String current) {
		this.current = current;
	}
	/**
	 * @return the min
	 */
	public String getMin() {
		return min;
	}
	/**
	 * @param min the min to set
	 */
	public void setMin(String min) {
		this.min = min;
	}
	/**
	 * @return the max
	 */
	public String getMax() {
		return max;
	}
	/**
	 * @param max the max to set
	 */
	public void setMax(String max) {
		this.max = max;
	}
	/**
	 * @return the startTimeInMillis
	 */
	public String getStartTimeInMillis() {
		return startTimeInMillis;
	}
	/**
	 * @param startTimeInMillis the startTimeInMillis to set
	 */
	public void setStartTimeInMillis(String startTimeInMillis) {
		this.startTimeInMillis = startTimeInMillis;
	}
	/**
	 * @return the useRange
	 */
	public String getUseRange() {
		return useRange;
	}
	/**
	 * @param useRange the useRange to set
	 */
	public void setUseRange(String useRange) {
		this.useRange = useRange;
	}
	/**
	 * @return the count
	 */
	public String getCount() {
		return count;
	}
	/**
	 * @param count the count to set
	 */
	public void setCount(String count) {
		this.count = count;
	}
	/**
	 * @return the sum
	 */
	public String getSum() {
		return sum;
	}
	/**
	 * @param sum the sum to set
	 */
	public void setSum(String sum) {
		this.sum = sum;
	}
	/**
	 * @return the value
	 */
	public String getValue() {
		return value;
	}
	/**
	 * @param value the value to set
	 */
	public void setValue(String value) {
		this.value = value;
	}
	/**
	 * @return the standardDeviation
	 */
	public String getStandardDeviation() {
		return standardDeviation;
	}
	/**
	 * @param standardDeviation the standardDeviation to set
	 */
	public void setStandardDeviation(String standardDeviation) {
		this.standardDeviation = standardDeviation;
	}
}
